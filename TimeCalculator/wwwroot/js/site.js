

function revealCode() {
	var section = document.getElementById("codeBlock");
	if (section.style.display === "none") {
		section.style.display = "block";
	} else {
		section.style.display = "none";
	}
}

function revealCalculator() {
	var section = document.getElementById("calculator");
	if (section.style.display === "none") {
		section.style.display = "block";
	} else {
		section.style.display = "none";
	}
}


